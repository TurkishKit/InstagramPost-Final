import UIKit


public extension UIColor {
    
    // MARK: Properties
    public class var instaBlue: UIColor {
        return UIColor(red:0.22, green:0.60, blue:0.95, alpha:1.00)
    }
    
    public class var instaLightGray: UIColor {
        return UIColor(red:0.86, green:0.86, blue:0.86, alpha:1.00)
    }
    
    public class var instaDarkGray: UIColor {
        return UIColor(red:0.60, green:0.60, blue:0.60, alpha:1.00)
    }
    
    
    public class var instaRed: UIColor {
        return UIColor(red:0.86, green:0.34, blue:0.36, alpha:1.00)
    }
}
